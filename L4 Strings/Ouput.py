#the same thing
print("Hello, World!")
print("Hello,", "World!")

#variable
x = "world!"
print("Hello,", x)

#changing the separator with sep =
print("this", "is", "a", "test.", sep = "")

#changing the behaviour at the end of the print statement
print("this", end = " ")
print("is", end = " ")
print("a", end = " ")
print("test.", end = " ")
