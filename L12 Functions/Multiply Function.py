def multiply(num1=1, num2=2, num3=3, num4=4, num5=5):
    '''Multiply funciton with multiple defaults
    '''
    return num1 * num2 * num3 * num4 * num5


print("multiply():", multiply())
print("multiply(1):", multiply(1))
print("multiply(1, 1):", multiply(1, 1))
print("multiply(1, 1, 1):", multiply(1, 1, 1))
print("multiply(1, 1, 1, 1):", multiply(1, 1, 1, 1))
