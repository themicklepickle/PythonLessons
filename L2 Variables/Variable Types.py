#dynamic typing

#first make it a string

testVar = "Spam Spam eggs and spam"
print("value of testVar: ", testVar, "type of testVar: ", type(testVar))

#second make it an integer

testVar = 2
print("value of testVar:" , testVar, "type of testVar: ", type(testVar))

#third make it a float

testVar = 2.5
print("value of testVar: ", testVar, "type of testVar: ", type(testVar))
